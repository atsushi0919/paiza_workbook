引き続き先読み・後読みについて見ていきます。

ここでは、否定後読みについて紹介します。否定後読みをすることで、指定した文字列が前に存在するものを除外することができます。

否定後読みは、正規表現の中で文字列を (?<! と ) で囲むことで行います。具体例を見てみましょう。
例えば、(?<!県)..市 という正規表現は、大阪府大阪市 という文字列の 大阪市 の部分にはマッチしますが、
神奈川県横浜市 という文字列の 横浜市 の部分にはマッチしません。

つまり、否定後読みは、前に指定した文字列が存在しないという条件を課しつつ、その部分はマッチさせないというようなことを実現できます。

文字列 S が与えられるので、正規表現を用いて、文字列 S において以下のパターンを検索し、最初にマッチした文字列の位置と、
そのパターンにマッチした文字列を出力してください。

パターン: 半角英大文字から始まり、記号 . で終わる、記号 [] を含まない 2 文字以上の文字列、
ただしその前に文字列 [Feature] が存在しないもので、できるだけ短くマッチさせる

ただし、検索対象のパターンが複数回出現する場合は、最初に出現する位置を出力してください。
検索は最左最短一致でおこなってください。
また、文字列の先頭の位置を 0 として、各文字の位置を左から順に 0, 1, 2, ... とします。


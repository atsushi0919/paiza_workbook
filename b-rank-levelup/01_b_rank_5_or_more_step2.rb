入力されたそれぞれの自然数 a_i を、そのまま出力してください。
最後は改行し、余計な文字、空行を含んではいけません。

条件
すべてのテストケースにおいて、以下の条件をみたします。

1 ≦ n ≦ 3,000
1 ≦ a_i ≦ 100,000


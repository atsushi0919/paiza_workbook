def solve(input_str)
  n, *a = input_str.split.map(&:to_i)

  # 選択済の色 全50種
  used_colors = Array.new(51, false)

  max_length = r = 0
  0.upto(n - 1) do |l|
    while (not used_colors[a[r]]) && r < n - 1
      used_colors[a[r]] = true
      r += 1
    end

    max_length = [max_length, r - l].max
    if r == l
      r += 1
    else
      used_colors[a[l]] = false
    end
  end
  max_length
end


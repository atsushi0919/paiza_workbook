input_lines = INPUT4.split("\n")
n, m, k = input_lines.shift.split.map(&:to_i)
a = input_lines.map { |l| l.split.map(&:to_i) }

s = Array.new(n + 1) { Array.new(m + 1, 0) }
0.upto(n - 1) do |i|
  0.upto(m - 1) do |j|
    s[i + 1][j + 1] = a[i][j] + s[i][j + 1] + s[i + 1][j] - s[i][j]
  end
end

max_value = 0
0.upto(n - k) do |i|
  0.upto(m - k) do |j|
    max_value = [max_value,
                 s[i + k][j + k] - s[i][j + k] - s[i + k][j] + s[i][j]].max
  end
end

puts max_value


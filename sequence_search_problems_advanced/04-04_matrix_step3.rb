京子ちゃんは友人とピクニックに行く予定です。行く予定の公園の敷地は南北 n メートル、東西 m メートルの長方形の形をしており、
n \* m 個の 1 辺 1 メートル正方形の区画で分けることができます。
公園は区画によって景色が異なり、それぞれ価値が決まっています。
北から i 番目、西から j 番目の区画を (i, j) と表し、(i, j) の価値を a_i_j とします。
また、ピクニックではレジャーシートを敷く予定です。レジャーシートは縦横 k メートルの大きさで、東西・南北にそれぞれ平行に敷きます。
京子ちゃんたちはもっとも景色のいい場所でご飯を食べたいので、
レジャーシートを敷く k \* k 個の区画の価値の合計が最大となるような場所に陣取ることにしました。
このときの k \* k 個の区画の価値の合計はいくつになるでしょうか？


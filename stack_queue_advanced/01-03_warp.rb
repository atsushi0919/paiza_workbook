input_lines = INPUT3.split("\n")
N, K = input_lines.shift.split.map(&:to_i)
# マス番号と index を対応
to = Array.new(N + 1) { Array.new(K + 1) }
input_lines.each.with_index(1) do |line, i|
  line.split.each.with_index(1) do |e, j|
    to[i][j] = e.to_i
  end
end

res = []
cc = 1
stack = []
1.upto(K) do |k|
  nc = to[cc][k]

  if nc > 0
    stack.push(cc)
    cc = nc
  else
    cc = stack.pop
  end
  res.push(cc)
end

puts res.join("\n")

=begin
paiza 君は次のような 1 〜 N の番号がつけられた N マスを移動します。
移動のルールは次の通りです。

マス x から i 回目の移動をおこなう場合、その移動先はマス to[x][i] となる。
to[x][i] が -1 の場合、現在のマスに移動してくる 1 つ前のマスが次の移動先となる。

paiza 君はマス 1 から移動を始めます。
マス 1 から K 回移動をしたとき、paiza 君が止まったマスの番号を止まった順に出力してください。

▼　下記解答欄にコードを記入してみよう

入力される値
N K
to[1][1] ... to[1][K]
...
to[N][1] ... to[N][K]


・1 行目ではマスの個数 N と移動する回数 K が半角スペース区切りで与えられます。
・2 行目からの N 行のうち i 行目では、to[i] の各要素が半角スペース区切りで与えられます。

入力値最終行の末尾に改行が１つ入ります。
文字列は標準入力から渡されます。 標準入力からの値取得方法はこちらをご確認ください
期待する出力
・プレイヤーが止まるマスを順に改行区切りで出力してください。

条件
すべてのテストケースにおいて、以下の条件をみたします。

・1 ≦ N ≦ 100
・1 ≦ K ≦ N
・to[i][j] = -1 または 1 ≦ to[i][j] ≦ N (1 ≦ i ≦ N, 1 ≦ j ≦ K)
・to[i][1] ≠ -1 (1 ≦ i ≦ N)
・移動先がなくなるような入力は与えられないことが保証されている


# 入力
N = 10 ** 8
n, *a = INPUT1.split.map(&:to_i)

num = a[0]
N.times do
  mb = num.to_s.chars.sort
  ma = mb.reverse
  num = ma.join.to_i - mb.join.to_i
end

p num


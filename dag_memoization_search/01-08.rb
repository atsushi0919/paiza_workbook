Q 個の整数 N_1, N_2, ..., N_Q が与えられます。あなたは各 N_i について、以下の操作の最終的な値を出力してください。


N_i の桁を入れ替えてできる最大の数を M_a 、最小の数を M_b とします。 N_i を M_a - M_b に置換します。
この置換処理を 10 の 100,000,000 乗回繰り返してください。

たとえば、 N_i が 213 だった場合、 M_a は 321 であり M_b は 123 です。
よって最初の操作で N_i は 321 - 123 = 198 に置換されます。その後、このような置換を 10 の 100,000,000 乗回繰り返します。



注意点として、 M_b のみ 0 から始まる数字として扱っても構いません。たとえば、 N_i が 90 のとき、 M_a は 90 で M_b は 09 となります。

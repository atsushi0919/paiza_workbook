# スーパー鳩時計
# https://paiza.jp/works/mondai/double_roop_problems/double_roop_problems__cuckoo_clock

(0..23).each {|h|
  (0..59).each {|m|
    i=(h+m)**4% -15
    puts "FIZZBUZZ\n"[i,i + 13]
  }
}

=begin
普通の鳩時計は 1 時間に 1 回しか鳴かないのでつまらないと思ったあなたは、鳩時計を改造してスーパー鳩時計を作りました。
このスーパー鳩時計は時刻が x 時 y 分のとき x + y が 3の倍数のとき"FIZZ"、
5 の倍数のとき"BUZZ", 3の倍数かつ5の倍数のとき "FIZZBUZZ" と鳴き、これらのいずれにも当てはまらなかった場合は鳴きません。
なお、0 は 3 の倍数かつ 5 の倍数であるとします。 0 時 0 分　〜 23 時 59 分 の各分のスーパー鳩時計の様子を出力してください。

入力される値
入力は標準入力にて以下のフォーマットで与えられます。

なし

条件
出力される値
0 時 0 分の鳩時計の鳴き声

0 時 1 分の鳩時計の鳴き声

...

23 時 58 分の鳩時計の鳴き声

23 時 59 分の鳩時計の鳴き声
0 時 0 分から 23 時 59 分までの各分の鳩時計の鳴き声を以上の形式で出力してください。
何も鳴かない時間は改行のみを出力してください。


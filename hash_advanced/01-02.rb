ハッシュ関数は様々なデータが入力として与えられます。画像もそのうちのひとつです。
例えば、画像のハッシュ値は同一画像の判定や類似度の計算に使うことができます。
本問では、画像を入力とするハッシュ関数を実装してみましょう。

. と # からなる縦 6 マス横 6 マスの大きさの画像 (picture) が与えられます。
以下のハッシュ関数 H を用いてハッシュ値を計算してください。

picture の i 行目 j 列目に対応する整数 i, j (1 ≦ i, j ≦ 6) を用いて、

H(picture) = 各 # の (i2 * j) の和 % 100

つまり、各 # についてその位置 i, j を求めて (i2 * j) を計算し、それらの和を 100 で割った余りがハッシュ値となります。

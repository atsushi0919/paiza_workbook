# ハッシュ値を返す
def hash(picture)
  tmp = 0
  picture.each.with_index(1) do |row, i|
    row.each.with_index(1) do |pixel, j|
      tmp += i ** 2 * j if pixel == "#"
    end
  end
  tmp % 100
end

# 入力
picture = $stdin.read.split("\n").map(&:chars)

# ハッシュ値を出力する
puts hash(picture)


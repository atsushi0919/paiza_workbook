process.stdin.resume();
process.stdin.setEncoding('utf8');

const lines = [];
const reader = require('readline').createInterface({
  input: process.stdin,
  output: process.stdout,
});
reader.on('line', (line) => {
  lines.push(line);
});
reader.on('close', () => {
  lines.shift();
  let a = lines.map((e) => {
    return parseInt(e, 10);
  });

  // 素因数分解
  const allDivs = [];
  const divsList = a.map((n) => {
    const divs = {};
    let div = 2;
    while (n > 1) {
      if (n % div > 0) {
        div++;
        continue;
      }
      divs[div] = (divs[div] || 0) + 1;
      n /= div;
    }
    allDivs.push(Object.keys(divs));
    return divs;
  });

  console.log(allDivs);

  // 公約数
  const comDivs = [...new Set(allDivs.flat())].filter((k) => {
    for (const divs of allDivs) {
      if (!divs.includes(k)) {
        return false;
      }
    }
    return true;
  });

  console.log(comDivs);

  // 最大公約数
  let gcf = 1;
  for (const k of comDivs) {
    let minMpr = Infinity;
    for (const divs of divsList) {
      minMpr = Math.min(minMpr, divs[k]);
    }
    gcf *= k ** minMpr;
  }
  console.log(gcf);
});

